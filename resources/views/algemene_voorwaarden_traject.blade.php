      <br>
      <p>Het <span class="font-bold">MEIT. Traject</span> is bedoeld om bewustwording, zelfliefde en persoonlijke groei te stimuleren. Deze voorwaarden zorgen voor helderheid, wederzijds vertrouwen en een fijne samenwerking tussen deelnemer en begeleider.</p>
      <p>Door deelname aan het <span class="font-bold">MEIT. Traject</span> verklaart de deelnemer zich akkoord met onderstaande voorwaarden.</p>
      <br>
      <h4 class="font-bold">Artikel 1 – Toepasselijkheid van deze voorwaarden</h4>
      <br>
      <p>1.1</p>
      <p>Deze algemene voorwaarden zijn van toepassing op alle overeenkomsten tussen <span class="font-bold">MEIT.</span> en haar deelnemers die betrekking hebben op deelname aan het <span class="font-bold">MEIT. Traject</span> of andere trajecten die door <span class="font-bold">MEIT.</span> worden aangeboden.</p>
      <br>
      <p>1.2</p>
      <p>In deze voorwaarden wordt met MEIT. Eveneens bedoeld eventuele samenwerkingspartners of derden die door MEIT. worden ingeschakeld voor de uitvoering van het traject.</p>
      <br>
      <p>1.3</p>
      <p>Afwijkingen van deze voorwaarden zijn alleen geldig als deze uitdrukkelijk en schriftelijk door <span class="font-bold">MEIT.</span> zijn bevestigd.</p>
      <br>
      <p>1.4</p>
      <p>Door inschrijving en betaling voor het <span class="font-bold">MEIT.</span> Traject verklaart de deelnemer zich akkoord met deze algemene voorwaarden.</p>
      <br>
      <h4 class="font-bold">Artikel 2 – Inschrijving en overeenkomst</h4>
      <br>
      <p>2.1</p>
      <p>Een overeenkomst tussen de deelnemer en <span class="font-bold">MEIT.</span> komt uitsluitend tot stand na een succesvolle online inschrijving via de website <a class="hover:underline underline-offset-2 text-second" href="https://www.meit.nl/" target="_blank">www.meit.nl</a> en nadat de betaling door <span class="font-bold">MEIT.</span> volledig is ontvangen.</p>
      <br>
      <p>2.2</p>
      <p>De betaling via de website geldt als definitieve bevestiging van deelname. Pas na ontvangst van deze betaling is de plek van de deelnemer in het traject officieel gereserveerd.</p>
      <br>
      <p>2.3</p>
      <p>De deelnemer ontvangt na betaling een bevestiging per e-mail met de startdatum, locatie en aanvullende informatie over het traject.</p>
      <br>
      <p>2.4</p>
      <p>Alle communicatie over het <span class="font-bold">MEIT. Traject</span> verloopt rechtstreeks via e-mail vanaf het domein @<span class="font-bold">meit.nl</span>, tenzij anders aangegeven.</p>
      <br>
      <p>2.5</p>
      <p><span class="font-bold">MEIT.</span> behoudt zich het recht voor om inschrijvingen te weigeren bij overinschrijving of andere gegronde redenen (zoals het overschrijden van de maximale groepsgrootte), in welk geval het betaalde bedrag volledig wordt teruggestort.</p>
      <br>
      <h4 class="font-bold">Artikel 3 – Rechten en plichten</h4>
      <br>
      <p>3.1</p>
      <p><span class="font-bold">MEIT.</span> verplicht zich om het <span class="font-bold">MEIT. Traject</span> met de grootste zorg, aandacht en professionaliteit uit te voeren. De begeleiding is gericht op persoonlijke groei, bewustwording en heling, maar blijft altijd een <span class="font-bold">inspanningsverplichting</span> en geen <span class="font-bold">resultaatgarantie</span>.</p>
      <br>
      <p>3.2</p>
      <p>Deelnemers blijven te allen tijde zelf verantwoordelijk voor hun eigen welzijn, keuzes, emoties en persoonlijke proces tijdens en na het traject. De begeleiding van <span class="font-bold">MEIT.</span> vervangt geen therapie of medische behandeling.</p>
      <br>
      <p>3.3</p>
      <p><span class="font-bold">MEIT.</span> behoudt zich het recht voor om derden of samenwerkingspartners in te schakelen bij de uitvoering van (delen van) het traject, mits dit de kwaliteit van het traject ten goede komt.</p>
      <br>
      <p>3.4</p>
      <p>Indien naar het oordeel van MEIT. een deelnemer onvoldoende verantwoordelijkheid neemt voor zijn of haar eigen proces, de sfeer of veiligheid van de groep verstoort, of herhaaldelijk niet deelneemt aan afgesproken onderdelen, behoudt <span class="font-bold">MEIT.</span> zich het recht voor om verdere deelname te weigeren zonder restitutie van het betaalde bedrag.</p>
      <br>
      <p>3.5</p>
      <p>Wanneer <span class="font-bold">MEIT.</span> afhankelijk is van informatie of medewerking van de deelnemer (bijvoorbeeld bij het aanleveren van geboortegegevens voor een profiel), is <span class="font-bold">MEIT.</span> niet aansprakelijk voor vertraging of onvolledige uitvoering indien deze informatie niet tijdig of juist wordt aangeleverd.</p>
      <br>
      <h4 class="font-bold">Artikel 4 – Annulering, verplaatsing en afwezigheid</h4>
      <br>
      <p>4.1</p>
      <p>Annulering van deelname aan het <span class="font-bold">MEIT. Traject</span> dient altijd <span class="font-bold">schriftelijk via e-mail</span> te gebeuren. De datum van ontvangst van de e-mail geldt als annulatiedatum.</p>
      <br>
      <p>4.2</p>
      <p>Bij annulering <span class="font-bold">meer dan 7 dagen vóór de startdatum</span> van het traject ontvangt de deelnemer <span class="font-bold">100% restitutie</span> van het betaalde bedrag.</p>
      <br>
      <p>4.3</p>
      <p>Bij annulering <span class="font-bold">binnen 7 dagen vóór aanvang</span> van het traject is restitutie <span class="font-bold">niet mogelijk</span>. De deelnemer behoudt in dat geval het recht om <span class="font-bold">éénmalig</span> de startdatum te verplaatsen naar een later traject (indien er plek is).</p>
      <br>
      <p>4.4</p>
      <p>Indien een deelnemer zonder afmelding niet verschijnt of tijdens het traject één of meerdere bijeenkomsten mist wegens ziekte, persoonlijke omstandigheden of andere redenen, vindt <span class="font-bold">geen restitutie</span> plaats. De gemiste bijeenkomst(en) kunnen niet worden ingehaald, tenzij dit uitdrukkelijk door <span class="font-bold">MEIT.</span> wordt aangeboden.</p>
      <br>
      <p>4.5</p>
      <p>Bij onvoorziene omstandigheden of overmacht van de zijde van <span class="font-bold">MEIT.</span> (zoals maar niet beperkt tot ziekte, noodweer, technische storingen of andere omstandigheden buiten haar invloedssfeer) behoudt <span class="font-bold">MEIT.</span> het recht om bijeenkomsten te verplaatsen of digitaal te laten plaatsvinden. Er vindt geen restitutie plaats.</p>
      <br>
      <p>4.6</p>
      <p>Wanneer naar het oordeel van <span class="font-bold">MEIT.</span> een deelnemer structureel verstorend gedrag vertoont of zich herhaaldelijk niet houdt aan de groepsafspraken, behoudt <span class="font-bold">MEIT.</span> zich het recht voor om verdere deelname te beëindigen, zonder recht op restitutie.</p>
      <br>
      <h4 class="font-bold">Artikel 5 – Betaling en facturatie</h4>
      <br>
      <p>5.1</p>
      <p>De betaling voor deelname aan het <span class="font-bold">MEIT. Traject</span> verloopt uitsluitend via de website <a class="hover:underline underline-offset-2 text-second" href="https://www.meit.nl/" target="_blank">www.meit.nl</a> of via de officiële betalingslink van <span class="font-bold">Stripe</span>.</p>
      <br>
      <p>5.2</p>
      <p>Een plek in het traject is pas <span class="font-bold">definitief gereserveerd na ontvangst van de betaling</span>. De betalingsbevestiging van Stripe geldt als officieel bewijs van inschrijving.</p>
      <br>
      <p>5.3</p>
      <p>De deelnemer ontvangt automatisch een bevestiging en factuur op het opgegeven e-mailadres.</p>
      <br>
      <p>5.4</p>
      <p><span class="font-bold">MEIT.</span> hanteert geen betaling op factuur of achteraf; deelname zonder voorafgaande betaling is niet mogelijk.</p>
      <br>
      <p>5.5</p>
      <p>In uitzonderlijke gevallen kan in overleg een gespreide betaling in termijnen worden toegestaan. Deze afspraken worden altijd schriftelijk bevestigd door <span class="font-bold">MEIT.</span></p>
      <br>
      <p>5.6</p>
      <p>Wanneer de deelnemer nalaat (een deel van) de betaling tijdig te voldoen, vervalt het recht op deelname aan het traject en is <span class="font-bold">MEIT.</span> gerechtigd de plek aan een andere deelnemer toe te wijzen.</p>
      <br>
      <h4 class="font-bold">Artikel 6 – Aansprakelijkheid en eigen verantwoordelijkheid</h4>
      <br>
      <p>6.1</p>
      <p>Deelname aan het <span class="font-bold">MEIT. Traject</span> is volledig op <span class="font-bold">eigen verantwoordelijkheid</span> van de deelnemer. De trajecten van <span class="font-bold">MEIT.</span> zijn gericht op persoonlijke ontwikkeling, bewustwording en heling, maar vervangen <span class="font-bold">geen medische of psychologische behandeling</span>.</p>
      <br>
      <p>6.2</p>
      <p><span class="font-bold">MEIT.</span> is niet aansprakelijk voor lichamelijke, emotionele of mentale klachten of andere schade die ontstaat tijdens of na deelname aan het traject. Deelnemers blijven te allen tijde zelf verantwoordelijk voor hun fysieke en emotionele grenzen.</p>
      <br>
      <p>6.3</p>
      <p><span class="font-bold">MEIT.</span> is niet aansprakelijk voor schade, verlies of diefstal van persoonlijke eigendommen op de locatie van het traject.</p>
      <br>
      <p>6.4</p>
      <p>Wanneer er tijdens het traject gebruik wordt gemaakt van ademwerk, meditatie of andere energetische oefeningen, is het aan de deelnemer om naar eigen inzicht en gevoel te bepalen of deelname op dat moment passend en verantwoord is.</p>
      <br>
      <p>6.5</p>
      <p>Eventuele aansprakelijkheid van <span class="font-bold">MEIT.</span> is in alle gevallen beperkt tot het bedrag dat de deelnemer heeft betaald voor het traject waarin de gebeurtenis plaatsvond.</p>
      <br>
      <h4 class="font-bold">Artikel 7 – Intellectueel eigendom en vertrouwelijkheid</h4>
      <br>
      <p>7.1</p>
      <p>Alle materialen, teksten, profielen, oefeningen, werkboeken, visuals en overige inhoud die binnen het <span class="font-bold">MEIT. Traject</span> worden gedeeld of verstrekt, zijn en blijven <span class="font-bold">eigendom van MEIT</span>.</p>
      <br>
      <p>7.2</p>
      <p>Deelnemers krijgen uitsluitend een persoonlijk gebruiksrecht op dit materiaal voor eigen ontwikkeling. Het is niet toegestaan om zonder schriftelijke toestemming van <span class="font-bold">MEIT.</span> enig deel van de inhoud te kopiëren, verspreiden, opnemen, publiceren, of te gebruiken voor commerciële of educatieve doeleinden.</p>
      <br>
      <p>7.3</p>
      <p>Alle persoonlijke informatie die deelnemers tijdens het traject delen — mondeling, schriftelijk of digitaal — wordt vertrouwelijk behandeld. <span class="font-bold">MEIT.</span> verplicht zich tot het zorgvuldig bewaren en beschermen van deze gegevens en zal deze nooit delen met derden zonder toestemming van de deelnemer, tenzij dit wettelijk verplicht is.</p>
      <br>
      <p>7.4</p>
      <p>Opnames (audio, foto of video) tijdens de bijeenkomsten mogen alleen worden gemaakt met expliciete toestemming van <span class="font-bold">MEIT.</span> en de overige deelnemers.</p>
      <br>
      <p>7.5</p>
      <p>Bij schending van deze voorwaarden behoudt <span class="font-bold">MEIT.</span> zich het recht voor om passende maatregelen te nemen, waaronder het beëindigen van deelname aan het traject zonder restitutie van het betaalde bedrag.</p>
      <br>
      <h4 class="font-bold">Artikel 8 – Overmacht</h4>
      <br>
      <p>8.1</p>
      <p>In geval van overmacht — waaronder begrepen maar niet beperkt tot ziekte, brand, storingen op de locatie, technische problemen, pandemie, of andere omstandigheden buiten invloed van <span class="font-bold">MEIT.</span> — behoudt <span class="font-bold">MEIT.</span> zich het recht voor om bijeenkomsten te <span class="font-bold">verplaatsen, pauzeren of annuleren</span>.</p>
      <br>
      <p>8.2</p>
      <p>In dergelijke gevallen zal <span class="font-bold">MEIT.</span> altijd streven naar een passend alternatief moment of oplossing.</p>
      <br>
      <p>8.3</p>
      <p>Overmacht geeft de deelnemer <span class="font-bold">geen recht op restitutie</span> van het betaalde bedrag, tenzij <span class="font-bold">MEIT.</span> het volledige traject annuleert zonder alternatief aan te bieden.</p>
      <br>
      <h4 class="font-bold">Artikel 9 – Toepasselijk recht en slotbepalingen</h4>
      <br>
      <p>9.1</p>
      <p>Op deze algemene voorwaarden en alle overeenkomsten die daaruit voortvloeien is het <span class="font-bold">Nederlands recht</span> van toepassing.</p>
      <br>
      <p>9.2</p>
      <p>Eventuele geschillen die voortkomen uit deelname aan het <span class="font-bold">MEIT. Traject</span> of uit de overeenkomst met <span class="font-bold">MEIT.</span>, worden in eerste instantie in <span class="font-bold">onderling overleg</span> opgelost.</p>
      <br>
      <p>9.3</p>
      <p>Indien partijen niet tot een oplossing komen, wordt het geschil voorgelegd aan de bevoegde rechter te <span class="font-bold">Rotterdam</span>.</p>
      <br>
      <p>9.4</p>
      <p>De meest recente versie van deze algemene voorwaarden, zoals gepubliceerd op <a class="hover:underline underline-offset-2 text-second" href="https://www.meit.nl/" target="_blank">www.meit.nl</a>, is steeds van toepassing op nieuwe inschrijvingen en overeenkomsten.</p>
