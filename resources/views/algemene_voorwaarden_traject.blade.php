        <p>Btw nummer: NL004425785B88</p>
        <p>Adres: Haydnstraat 10, 2901 HK, Capelle aan den IJssel</p>
        <p>Email: <a class="hover:underline underline-offset-2 text-second" href="mailto:Info@meit.nl">Info@meit.nl</a></p>
        <p>Website: <a class="hover:underline underline-offset-2 text-second" href="https://www.meit.nl/" target="_blank">www.meit.nl</a></p>
